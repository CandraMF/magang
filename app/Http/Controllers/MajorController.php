    public function search($payload)
    {
        $data = Major::where('name', 'ilike', '%'.$payload.'%')
            ->get()
            ->map(function($q) {
                return [
                    'name' => $q->name,
                    'major_id' => $q->major_id
                ];
            });

        return response()->json($data);
